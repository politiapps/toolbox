/** Replace `target`'s contents with the named icon as an inline SVG. */
export function setIcon(target: HTMLElement, name: string): void {
	const path = ICONS[name];
	if (!path) return;
	target.innerHTML = `<svg xmlns="http://www.w3.org/2000/svg" width="24" height="24" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round">${path}</svg>`;
}

/** A button element that shows only an icon. */
export function iconButton(name: string, ariaLabel: string): HTMLButtonElement {
	const btn = document.createElement("button");
	btn.className = "icon-button";
	btn.setAttribute("aria-label", ariaLabel);
	setIcon(btn, name);
	return btn;
}
